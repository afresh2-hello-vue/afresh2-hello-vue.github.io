<script setup>
import { ref } from 'vue'

const awesome = ref(true)

function toggleAwesome(v) {
awesome.value = !v
}

function isAwesome () {
return awesome.value
}
</script>

<template>
	<button @click="toggleAwesome(awesome)">toggle</button>

	<h1 v-if="isAwesome()">Vue is awesome!</h1>
	<h1 v-else>Oh no 🥲</h1>

	//이렇게 쓰면 동작하지 않는다.
	<h2 v-if="isAwesome">v-if TRUE</h2> 
	<h2 v-show="isAwesome()">v-show TRUE</h2>
</template>
